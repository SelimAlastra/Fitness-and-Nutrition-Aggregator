  useEffect(() => {
      dispatch(getPosts());
  }, [currentId,dispatch]);
  const posts = useSelector((state) => state.posts);

  /**
   * get searched posts
   */
  const getSearchPosts = () => {
    const searchPosts = posts.filter(post => post.tags.find(tag => tag === "nutrition"));

    searchPosts.forEach(element => {
      console.log(element);
    });
  };

  const classes = useStyles();
